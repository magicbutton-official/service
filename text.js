// Функция для получения строки на определенном языке с возможностью вставки значений
function getString(key, lang, values = {}) {
    // По умолчанию используется английский язык
    lang = lang || "en";

    // Проверяем наличие строки для данного ключа и языка
    if (strings[key] && strings[key][lang]) {
        // Получаем строку
        var string = strings[key][lang];

        // Заменяем все вхождения {{placeholder}} на значения из объекта values
        for (var placeholder in values) {
            string = string.replace(new RegExp('{{' + placeholder + '}}', 'g'), values[placeholder]);
        }

        return string;
    } else {
        // Если строка не найдена, возвращаем null
        return null;
    }
}

